# Problem #3 this code will randomly select a day of the week from the days_of_the_week

import random

days_of_the_week = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"]
random_day = random.choice(days_of_the_week)  # Select a random day from the list.
print(random_day)  # Print the randomly selected day.
